public class G2MathTrigonometria {
    public static void main(String[] args) {
        //Pasar de Radianes a grados

        System.out.println(" 1.2 Radianes a grados: "+(Math.toDegrees(1.2)));

        //De grados a Radianes
        System.out.println(" 70 Grados a radianes : "+(Math.toRadians(70)));

        //Para Seno se usa sin pero siempre toma en radianes, no en grados
        System.out.println("Seno de 1.3 Radianes:"+Math.sin(1.3));
        //Coseno
        System.out.println("Coseno de 1.3 Radianes:"+Math.cos(1.3));




    }
}
